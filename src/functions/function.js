export const setColor = (type)=>{
    switch (type){
      case "poison":
        return "#340A5E";
      case "fire":
        return "#EAAB7D";
      case "water":
        return "#71C3FF";
      case "bug":
        return "#76A866";
      case "normal":
        return "#BF9762";
      case "flying":
        return "#004170";
      case "electric":
        return "#e8e400";
      case "grass":
        return "#729F92";
      case "psychic":
        return "#C596BD";
      case "fairy":
        return "#EE99AC";
      case "dark":
        return "#707070";
      case "rock":
        return "#665a39";
      case "ground":
        return "#e0b700";
      case "fighting":
        return "#b31e1e";
      
    }
  }